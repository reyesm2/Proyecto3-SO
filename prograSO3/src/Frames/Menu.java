    private void jButton_ejecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ejecutarActionPerformed
        // TODO add your handling code here:

        cambiarEstadoAlgoritmos();

        String inicio = this.jTextField_inicio.getText();
        String total = this.jTextField_total.getText();
        String grupos = this.jTextField_total1.getText();
        
        
        int direccion = 0 ;
        String direccionString = (String)this.jComboBox_dir.getSelectedItem();
        if(direccionString.equals("Incremento")){
            direccion = 1; 
            
        }
        else{
            direccion = 0;
        }

        System.out.println("Direccion: " + direccion);
        
        
        
        if(!inicio.equals("")){
            if(!total.equals("")){
                if(!grupos.equals("")){
                    String requisicion = (String)this.jComboBox_requisiciones.getSelectedItem();
                    if(!requisicion.isEmpty()){
                        int re = Integer.parseInt(requisicion);
                        Singleton.getInstance().getControlador().ejecutarAlgoritmos(re,
                            Integer.parseInt(inicio),
                            Integer.parseInt(total),
                            direccion,
                            Integer.parseInt(grupos));
                    }
                    mensajeDialog("Se ejecutaron los algoritmos.", "Ejecuci√≥n existosa.");
                }else{
                    mensajeDialog("Complete el tama√±o grupos", "Datos incompletos");
                }
            }else{
                mensajeDialog("Complete el total", "Datos incompletos");
            }
        }else{
            mensajeDialog("Complete el inicio", "Datos incompletos");
        }

    }//GEN-LAST:event_jButton_ejecutarActionPerformed

